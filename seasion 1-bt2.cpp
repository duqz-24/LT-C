#include <stdio.h>

int main() {
    int number1, number2, sum;

    // Khởi tạo giá trị cho hai số
    number1 = 10;
    number2 = 20;

    // Tính tổng và gán vào biến sum
    sum = number1 + number2;

    // In kết quả ra màn hình
    printf("Tổng của %d và %d là: %d\n", number1, number2, sum);

    return 0;
}